class Calc:
    def sum(self,x,y):
        print(x,y)

    def mul(self,x,y):
        print(x*y)


class SciCalc:
    def sum(self,x,y):
        print(x+y)

    def mul(self,x,y):
        print(x*y)

    def pow(self,x,y):
        print(x**y)

s = SciCalc()
s.sum(2,3)
s.mul(4,5)
s.pow(2,3)









